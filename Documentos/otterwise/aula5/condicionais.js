const value = 1

let bool1 = true
let bool2 = false
//IF
if (bool1 || bool2) {
  console.log('Condição verdadeira')
} else {
  console.log('Condição falsa')
}

console.log(value)

//SWITCH
switch (value) {
  case 1:
    console.log('É 1')
    break
  case 2:
    console.log('É 2')
    break
  default:
    console.log('Não é nenhum dos valores acima')
    break
}

//Else If
if (value === 1) {
  console.log('É 1')
} else if (value === 2) {
  console.log('É 1')
} else {
  console.log('Não é nenhum dos valores acima')
}
